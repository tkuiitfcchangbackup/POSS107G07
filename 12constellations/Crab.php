巨蟹座的缺點

  跟著情緒走，提不起放不下、太過多愁善感、不知適可而止、缺乏理性思考、經不起打擊、說話拐彎抹角、不直接，過度保護自己、沉溺於往事、無法面對事實、心腸太軟、天性多疑且情緒化致難以取悅、嫉妒心強並有戀母情結、可能因過度敏感而導致自憐、個性善變、不穩定、有時因生活態度太認真而失之無趣、心胸狹窄、苛刻、貪吃、邋遢、喜歡被奉承。果斷力弱．情緒他型巨蟹座是所有星座中最為情緒化，容易感情用事，為了感情而改變被衷，由於防衛比態很強，會讓人會有些不近人情。

