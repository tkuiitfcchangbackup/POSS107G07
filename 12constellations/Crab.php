巨蟹座的優點

   情感真摯深切、想像力豐富、念舊、重情義、有包容力、直覺敏銳、懂得體貼、關懷、親切、溫暖、善解人意、有同情心、善良、熱心、敏感、富有同情心；長於記憶、腦筋敏銳、領悟力好、適應力佳、有高度的想像力；具強烈的母性或父性的本能、保護色彩濃厚、謹慎、節儉；有堅強意志力和耐力，不屈不撓；理財觀念甚佳；愛國；忠於愛情，重視家庭的溫暖與安定，擅理家務，重視家庭的和諧，是所有星座中最具家庭觀念的星座。

親切．有親愛的本能及家的觀念型巨蟹座總會自然散發出親愛的氣質，像母愛般的溫柔，特別喜歡照顧別人，沒有野心的巨蟹座是個很有家庭念，喜歡和家人和樂融融地過著安穩的日子。此外巨蟹座重視實際生活，在任何環境下總是充滿生命力的現實主義者。
