獅子座的優點

  有領導能力、具有激勵人心的氣質、組織力強、熱情開朗、對人慷慨、大方、心胸寬大，懂得寬恕、一言九鼎、有信用、樂觀、不多疑、誠懇正直。天生具天者風範的獅子座，有很強的領導能力，善於將自己最好的一面表現出來，行動積極做事總是充滿信心，是屬於領導型的人物。


