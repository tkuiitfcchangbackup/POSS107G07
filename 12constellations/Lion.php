<p>

獅子座的人，性格相當於百獸之王的獅子，堅強、可靠、驕傲、寬大。他的一切做為都很誠實，對於依賴他的人，即使犧牲自己，也會妥善照顧對方。由於獅子座的人，天生就具有王者的氣質，所以的確也能了解別人的痛苦。不過，有時候他又好似要忘掉寂寞與虛偽一般，而沈迷於遊樂之中。因有著脾氣暴躁、頑固，以及不願意妥協的個性，所以這點，是獅子座的人需要特別注意的。為人正直，頗具威嚴。喜歡以自己的魅力和才能開創出一片天地，並熱衷於權力地位。處事採用光明磊落全力以赴的做法，厭惡卑劣的小人行徑。有演戲的才華，對自己充滿自信，近乎自戀。另一方面，由於心胸寬大，自能吸引群眾。不過，容易被自己的情緒左右，經常覺得孤獨。

獅子座的人個性衝動，熱情，誠實而忠心，理想很高，莊嚴又偉大，有幽默感，天性快樂，會吸引很多人，對人很有禮貌而體貼。大方，性急，不會記恨太久，有活潑的想像力，興趣廣泛，健談，有很好推理和判斷能力，也有創造力，有領導力，做人熱誠，衝動，動人的格調，有藝術的天賦，表演天才，個性明朗，光明正大，威風，自信勇敢，熱心，博愛，是一位領導人物。但也有自私、自大、易怒、喜怒無常、虛榮、浪費等缺點。有繁華熱鬧的一面，也有黑暗、寂寞、孤獨的一面。

性情高傲，有種貴族的氣質，心地高尚而寬宏大量，生性不願委屈自尊去做暗中的勾當，或做卑賤的事情。你信任別人，也要求別人絕對的對你採取信任的態度。你喜歡受到別人的信靠，也喜歡掌握權威。對工作勤奮，但不願居於被管轄的地位。對於重覆而單調的事情感到厭煩，也不喜歡繁雜瑣碎的細節。

獅子座的人，一般而言很強壯，深具野心與專橫。對別人的挑戰，你是光明正大而公平的，絕不採取陰私的對策。此座的人有充份的自信心，對自己有信念。但也要求別人的稱讚、非常自負，以為自己本身的一切是世界上最優秀的。喜歡感情上全心的投注，也會將愛情坦率的表現出來。又本性富於冒險精神，能夠面對任何追求理想時所遭遇的危險。

