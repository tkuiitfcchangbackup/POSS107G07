獅子座的缺點

  死愛面子活受罪、好大喜功、莫名的優越感、喜歡接受奉承、缺乏節儉的美德、喜歡指揮別人、缺乏耐性、剛愎自用，自以為是、緬懷過去、能伸不能屈。


   由於與生俱有領導本能很強的獅子座，在團體中喜歡以領導者自居，並喜歡表現自己，難免表現給有自傲的態度，並且不喜歡讓自己驅使在別人的使喚下，凡事總以自我為中心．虛榮心很強的獅子座喜歡被別人讚美並求好表現，因而會自我膨漲，反而會遭對方反感，而使人敬而遠之。
