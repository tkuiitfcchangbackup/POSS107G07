<p>

雙子座的人，在性格方面的最大特徵便是具有極敏銳的觀察力。雙子座的人，手藝十分靈巧，在各方面也都能表現出自己的才能。然而另一方面來說，也就犯了樣樣都懂，卻樣樣不精的毛病。強烈的好奇心，和不斷吸收新知識的慾望，會使雙子座的人經常保持年輕、且富有魅力，使得眾多的朋友圍繞在他的四周。個性敏銳又快捷。有強烈的好奇心和求知慾，對於新觀念和新流行的感觸十分敏銳。聰明機智，有辯才，是一個謀略家和演說家。遇事都能妥善對應，冷靜觀察，果敢而有擔當。而且常會有一些突發奇想的點子，有大膽假設，小心求證的個性。
  雙子座的一個性格是脾氣乖戾的藝術家態度，另一個性格是明朗的社交家，這種雙重性格會使雙子座的人，生活發生矛盾，喜歡幻想，易滿足於沈思和白日夢。但富有靈感和想像力，是故，這星座的人有很多作家，新藝術的創始者，發明家，作曲家等等。優點是有潛在的藝術才能，缺點是有拖延的傾向，自私，不關心別人的感情和思想，神經質，不好相處等。
  雙子座的人有多方面的才能，動作快而有力，直覺而進取，沒有掛慮，愉快而活潑，伶俐而善變。善言雄辯，思想獨特，想像力強，有幽默感，有良好的觀察力和領悟力，富有機智。外觀很熱誠，但心中卻很冷。
  喜歡擁有兩種職業，兩種羅曼史，喜歡富於變化的生活，喜歡經常的旅行。對不同環境具有很大的適應力，善於隨機應變。此星座者比較受到理智所控制，不容易動感情的，很少經歷深刻的愛情。

