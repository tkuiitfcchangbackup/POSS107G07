雙子座的缺點

   三分鐘熱度、善變、處世缺乏原則、舉一反十，過於神經質、做事蜻蜓點水不深入、過於圓滑、容易緊張、意志不堅定、讓人覺的不可靠、不專心、懷疑心重、善變、雙重性格、缺乏耐性、狡猾、不安份、過人的精力未能發洩時則脾氣暴躁且喋喋不休。不穩重．做事半途而癈型。由於雙子座很愛說話，常會給人有輕浮不穩重的感覺，個性變他多端，讓人捉摸不定而感到厭煩，由於好奇心強，總是喜歡多方面試，但都無法貫徹到底，而且無論在生活或感情上，都喜歡多變，化屬於玩樂型。

