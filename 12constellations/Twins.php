雙子座的優點

   多才多藝、見人說人話、見鬼說鬼話、足智多謀、反應靈敏、八面玲瓏、善於交際、懂得隨機應變、充滿生命力、擅長溝通、知進退，有分寸、適應力強、風趣幽默、適應力強、機智、敏捷、喜歡忙碌和變化、主動、活潑而健談、聰慧且多才多藝、具有寫作和語言方面的天才、對時尚有著敏銳的感受力，能夠永久維持著年輕和時髦的外貌。有廣博的學術涵養．適應力佳型。
   雙子座的人能言善道又善於交際，很容易跟別人打成一片，是個社交型且愛歡迎的人物，由於頭腦靈活，作事能隨機應變，並且有強烈的研究心，因此再困難的事情，他都能化繁為簡。

