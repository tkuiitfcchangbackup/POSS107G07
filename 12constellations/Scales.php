天秤座的優點

公平客觀、有正義感、適應力強、對美感有鑑賞力、邏輯強，善分析、天生的優雅風采、浪漫的戀愛高手、有外交手腕、因事制宜，能屈能伸，適應力強。
    天秤座處事都以中庸主義，常常是和平的協調者，對任何人具有公正的關懷心，因此天秤座可以置於公平立埸上，發揮社交官的管理能力，而且有很強的抑制能力，可以控制自己內心情緒，總是保持溫和的態度待人。

