天秤座的缺點

優柔寡斷，猶豫不決、意志不堅定，容易受人影響、鄉愿，怕得罪人、不能承受壓力，沒有擔當、過份要求公平，吃不得虧、息事寧人，治標不治本、總是自圓其說，藉口太多、喜歡享受，好逸惡勞、常不經意地亂放電、缺乏自省能力。

天秤座有虛榮的一面，不喜歡顯示自己的缺點，總會把最好的一面表現出來，而且過份重視自己的外表，喜歡花錢在打分上，有時會被人誤為虛榮很重，做事有時也會猶豫不決難下抉擇，而失去很多機會。


